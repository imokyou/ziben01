        <h1>风险纰漏</h1>
        <dl>
            <dd>这个文件是CAPITAL COUNTRY TECHNOLOGY INC 披露（“公司”），以提供capital服务客户协议的条款和条件下的客户协议，涉及的潜在风险金融市场。客户首先应该意识到与此相关的潜在损失活动。然而，由于广泛的可能的情况下，本文档不能透露固有的所有风险金融市场交易。在本文档中使用的术语具有相同的含义定义在包括所有位于“监管文件”部分的所有文件的相应规定capital及本公司网站。</dd>
            <dt>一.“杠杆效应”</dt>
            <dd>1.1. 在融资融券交易条件下执行交易操作时，即使是小市场的变动也可能会对客户的交易帐户由于杠杆作用的影响有很大的影响。客户必须考虑，如果市场上的趋势是对他们，客户可能会维持他们的初始保证金和任何额外的损失存放资金保持开放位置。客户对所有风险都应承担全部责任，财务资源利用与选择的交易策略。</dd>
            <dd>1.2. 我们强烈建议保持保证金水平不低于1000%，以及配售止损限制潜在损失的订单。</dd>
            <dt>二. 高挥发性的仪器</dt>
            <dd>2.1. 许多工具都在很宽的范围内交易日内价格变动，所以客户必须仔细考虑这样一事实：不仅有很高的利润率，而且也有损失。</dd>
            <dt>三. 技术风险</dt>
            <dd>3.1. 客户应承担因信息、通讯、通讯失败而造成的财务损失的风险，电子和其他系统。</dd>
            <dd>3.2. 在通过客户终端执行交易业务时，客户应承担财务风险损失，这可以引起：<br>a.客户端硬件、软件和互联网连接的失败；<br>
                b.客户设备的操作不当；<br>
                c.在客户端终端中的错误设置；<br>
                D.延迟的客户端终端更新；<br>
                e.客户的无知在capital服务的用户指南和帮助描述的适用规则第一节。</dd>
            <dd>3.3.客户承认，在峰值负载的时刻，可能会有一些困难与值班接线员的电话沟通，尤其是在快速市场（例如，当钥匙经济指标公布）。</dd>
            <dt>四. 不正常的市场条件下</dt>
            <dd>4.1.客户必须承认在异常的市场条件下，客户的执行时间指令可能会增加。</dd>
            <dt>五. 交易平台</dt>
            <dd>5.1.客户应承认只有一个请求或指令被允许在队列中。一旦客户端发送了一个请求或指令，客户端发送的任何其他请求或指令将被忽略。在“订单”窗口中，“订单被锁定”的消息将出现。</dd>
            <dd>5.2.客户端必须承认，引用信息的唯一可靠的来源是客户端的服务器有现场帐户。在客户终端的报价基础不被认为是一个可靠的报价来源信息，如在客户端和服务器之间的一个坏连接的情况下，一些报价只可能无法到达客户端。</dd>
            <dd>5.3.客户端必须承认，当客户端关闭窗口时，修改/删除一个命令，如以及打开/关闭一个位置的窗口，已发送到服务器的指令或请求将不会被取消。</dd>
            <dd>5.4.客户端应承担在客户端发送的情况下执行非计划性事务的风险在接收先前发送的指令之前的另一个指令。</dd>
            <dd>5.5.客户端必须承认，如果一个命令已经被执行，但客户端发送一个指令修改一个待定的订单的水平和停止损失和/或在同一时间的利润订单的水平，只有将执行的指令是修改停止损失和/或采取的利润水平上的指令在那个订单上打开的位置。</dd>
            <dt>六. 通信</dt>
            <dd>6.1.客户应承担由客户造成的任何财务损失的风险，或没有收到通知从公司。</dd>
            <dd>6.2.客户应承认未加密的信息通过电子邮件传播不受未经授权的访问。在本文件发布，这是位于http://www.capitalrev.com 。</dd>
            <dd>6.3.客户应同意，公司有权删除发送给客户的邮件通过内部邮件3天之后，他们已被发送，尽管事实上，客户端可能没有收到他们还。</dd>
            <dd>6.4.客户应坚持从公司获得信息的保管负全部责任假定未经授权的访问客户的交易帐户的任何财务损失的风险为三分之一聚会。</dd>
            <dt>七. 不可抗力</dt>
            <dd>7.1.客户应承担因不可抗力造成的财务损失的一切风险。</dd>
            <dt>八. 法定禁止和限制</dt>
            <dd>8.1. 当完成操作（或与之相连的操作时，客户应承担所有财务和其他风险这些操作）对金融市场法律所禁止或限制的立法，客户为居民的国家。CAPITAL COUNTRY TECHNOLOGY INC </dd>
