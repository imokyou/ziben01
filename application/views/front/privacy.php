            <dt>个人信息采集</dt>
            <dd>公司收集必要的客户信息，以便开设客户交易账户、执行交易、保护客户资产和隐私安全并向客户提供所需的服务。在该方面，公司从客户，在某些情况下，从银行、信贷机构、清算机构或能够帮助公司掌握客户需求和偏好的其他来源采集信息，以便有效服务客户。公司采集的客户信息包括识别客户和与客户沟通所需要的信息。公司也可收集某些个人基本信息，包括出生日期、教育背景、从事职业等。同时公司还会对交易相关信息进行评估</dd>
            <dt>个人信息使用</dt>
            <dd>公司对客户个人信息的使用仅以提供高质量服务和保证客户安全为目的。此类信息能够帮助公司提高服务质量、定制浏览体验并将与客户相关的新产品、服务或优惠活动及时传达给客户；在该方面，客户也同意出于此类目的的信息使用。如果客户出于任何原因不希望接收此类性质的信息，可以通过如下邮箱与公司取得联系：【邮箱地址】</dd>
            <dt>个人信息保护</dt>
            <dd>客户向公司提供的任何个人信息均将被视为保密文件，仅用于公司内部及其关联公司，且如果不经监管程序或法律程序批准，不得向任何第三方披露该信息。对于经法律或管理机构批准认为有必要披露的信息，除非管理机构另有说明，否则须遵循“须知”原则。在此类情况下，公司应将该信息的机密性清楚地告知第三方。</dd>
            <dt>关联公司与合作伙伴</dt>
            <dd>如果出于向客户提供产品或服务的目的，且关联公司提出合理要求，公司可与其共享客户信息。为了提供满足客户需求的、类似的额外产品和服务，公司可与其合作伙伴、关联公司和附属公司共享客户信息，但须在获得客户授权的情况下，有意义有目的地移交。</dd>
            <dt>非关联第三方</dt>
            <dd>除该隐私保护政策表述的情况外，公司不得通过出卖、出租或其他方式向第三方披露客户个人信息。<br>
                公司保留在法律、规章允许和政府授权的情况下向第三方披露客户信息的权利。同时，公司可披露征信报告生成所需信息或收债代理商出于为公司及其客户提供服务之目的所需信息。此外，公司可协助第三方完成某些内部职能，如账户处理、政策执行、客户服务、客户满意度调查或与其业务相关的其他数据采集活动。客户信息的分享和使用应严格遵循上述执行规范，且不得用于任何其他目的。所有公司与之共享客户信息的第三方均需自觉按照相关法律要求和与公司类似的保护方式，保护此类客户个人信息。公司不得与其认为不能向客户提供相应水平保护的第三方共享客户信息。<br>
                如果客户由业务介绍人引进，则该业务介绍人有权获知客户信息。因此，客户同意与该业务介绍人共享信息。</dd>
